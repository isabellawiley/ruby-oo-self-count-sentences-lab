require 'pry'

class String

  def sentence?
    if self.end_with?(".")
      true
    else 
      false
    end
  end

  def question?
    if self.end_with?("?")
      true
    else 
      false
    end
  end

  def exclamation?
    if self.end_with?("!")
      true
    else 
      false
    end
  end

  def count_sentences
    delimeter = Regexp.union(".", "?", "!")
    arr = self.split(delimeter)
    arr.delete("")
    arr.length
  end
end